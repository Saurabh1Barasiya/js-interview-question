//  create a new Array .

let arr = new Array(1,2,3,4);
console.log(arr);           // [ 1, 2, 3, 4 ]


// we can create an array without new keyword.

let myArr = Array(10,20,30);
console.log(myArr);        //    [ 10, 20, 30 ]



let newArr = [100,200,300];
console.log(newArr);        //   [ 100, 200, 300 ]